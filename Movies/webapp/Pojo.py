class Movie:
    def __init__(self, title, year, category, country, image, stock, price):
        self.title = title
        self.year = year
        self.category = category
        self.country = country
        self.image = image
        self.stock = stock
        self.price = price


class User_Class:
    def __init__(self, id_user, name, last_name, phone, age, role, image, username, password):
        self.id_user = id_user
        self.name = name
        self.last_name = last_name
        self.phone = phone
        self.age = age
        self.role = role
        self.image = image
        self.username = username
        self.password = password
